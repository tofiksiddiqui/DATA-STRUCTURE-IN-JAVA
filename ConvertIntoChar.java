public class ConvertIntoChar {
    public static void main(String[] args) {
        int num1 = 65;
        System.out.println("Before Converting int character : " + num1);
        char ch1 = (char) num1;
        System.out.println("After Converting into character : " + ch1);
        int num2 = 97;
        System.out.println("Before Converting int character : " + num2);
        char ch2 = (char) num2;
        System.out.println("After Converting into character : " + ch2);
    }
}
